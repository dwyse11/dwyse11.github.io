const nameHolder = document.querySelector('#cityName');

const size = cities.length;

const getRndInteger = (size) => {
    return Math.floor(Math.random() * size);
}

const changeName = () => {
    const rndCity = cities[getRndInteger(size)];
    nameHolder.innerText = rndCity;
}

const btnChange = document.querySelector('#changeName');

btnChange.addEventListener('click', (event) => {
    changeName();
});

